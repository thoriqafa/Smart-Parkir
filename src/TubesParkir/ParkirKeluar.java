    private void txtPlatKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPlatKeyPressed
        int key = evt.getKeyCode();
        if (key == KeyEvent.VK_ENTER) {
            Toolkit.getDefaultToolkit().beep(); 
            if (txtPlat.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Masukkan Data!");
            } else {
                try
                {
                    getData();
                    String sql="select * from TblParkir where IDKendaran='"+txtPlat.getText()+"' and Biaya IS NULL";
                    Statement st=con.createStatement();
                    ResultSet rs=st.executeQuery(sql);

                    while(rs.next())
                    {
                        txtId.setText(rs.getString("IDParkir").replaceAll(" ", ""));
                        txtLok.setText(rs.getString("IDLokasi").replaceAll(" ", ""));
                        txtJamMsk.setText(rs.getString("JamMasuk").replaceAll(" ", ""));
                        txtJamKeluar.setText(rs.getString("JamKeluar").replaceAll(" ", ""));
                        txtStaff.setText(rs.getString("IDStaff").replaceAll(" ", ""));
                    }
                }catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null,e);
                }  
            }
        }
    }//GEN-LAST:event_txtPlatKeyPressed

