    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try {
            String by1 = txtJamKeluar.getText();
            String by2 = txtJamMsk.getText();
            
            DateFormat formatter = new SimpleDateFormat("HH:mm:ss");
            
            java.sql.Time klr = new java.sql.Time(formatter.parse(by1).getTime());
            java.sql.Time msk = new java.sql.Time(formatter.parse(by2).getTime());
            
            long fu = klr.getTime()-msk.getTime();
            long diffHours = fu / (60 * 60 * 1000);
            if (diffHours==0) {
                long tot = 1*5000;
                String biaya=String.valueOf(tot);
                txtBiaya.setText(biaya);
            } else {
                long tot = diffHours*5000;
                String biaya=String.valueOf(tot);
                txtBiaya.setText(biaya);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnHitungActionPerformed

