    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try{

            long millis = System.currentTimeMillis();
            
            String sql="insert into TblParkir (TglParkir, JamMasuk, IDKendaran, IDStaff, IDLokasi) values(?,?,?,?,?)";
            java.sql.PreparedStatement pst=con.prepareStatement(sql);
            pst.setDate(1, new java.sql.Date(millis));
            pst.setTime(2, new java.sql.Time(millis));
            pst.setString(3, txtPlat.getText());
            pst.setString(4, txtStaff.getText());
            pst.setString(5, txtLok.getText());
            
            pst.execute();
            
            String sql1 ="update TblLokasiParkir set Status = 1 where IDLokasi = '"+txtLok.getText()+"'";
            
            java.sql.PreparedStatement pst1=con.prepareStatement(sql1);
            pst1.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil disimpan");
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

