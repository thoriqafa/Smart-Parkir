    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        if (txtPlat.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Masukkan Data!");
        } else {
            try {
            String sql="select * from TblKendaraan where IDKendaraan like '%" + txtPlat.getText() + "%'";
            
            java.sql.PreparedStatement pst=con.prepareStatement(sql);
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Data sudah ada!");
            }else{
                int tanya = JOptionPane.showConfirmDialog(null, "Data tidak ada. Apakah ingin dimasukkan?","Konfirmasi",JOptionPane.YES_NO_OPTION);
                if (tanya == JOptionPane.YES_OPTION) {
                    String sql1="insert into TblKendaraan (IDKendaraan) values('"+txtPlat.getText()+"')";

                    Statement st = con.createStatement();
                    java.sql.PreparedStatement pst1 =con.prepareStatement(sql1);
                    pst1.execute();

                    JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                } else {
                    txtPlat.setText("");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        
        
    }//GEN-LAST:event_btnCariActionPerformed

